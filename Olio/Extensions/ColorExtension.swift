import Foundation
import SwiftUI

extension Color {
    /// Wrapped UIKit UIColor value giving SwiftUI built-in semantic colour for systemGroupedBackground.
    static let systemGroupedBackground = Color(UIColor.systemGroupedBackground)

    /// Wrapped UIKit UIColor value giving SwiftUI built-in semantic colour for secondarySystemGroupedBackground.
    static let secondarySystemGroupedBackground = Color(UIColor.secondarySystemGroupedBackground)
}
